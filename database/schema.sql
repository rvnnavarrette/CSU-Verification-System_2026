-- SECTION 10: USEFUL VIEWS (optional, for reports/exports)
-- ============================================================

-- View: pending requests with submitter email
CREATE OR REPLACE VIEW public.v_pending_requests AS
SELECT
    vr.id,
    vr.student_name,
    vr.degree_diploma,
    vr.student_status,
    vr.school_name,
    vr.created_at,
    u.email AS submitter_email
FROM public.verification_requests vr
JOIN public.users u ON u.id = vr.user_id
WHERE vr.status = 'pending'
ORDER BY vr.created_at ASC;

-- View: full request summary for admin dashboard
CREATE OR REPLACE VIEW public.v_all_requests AS
SELECT
    vr.*,
    u.email          AS submitter_email,
    u.display_name   AS submitter_display_name
FROM public.verification_requests vr
JOIN public.users u ON u.id = vr.user_id
ORDER BY vr.created_at DESC;

COMMENT ON VIEW public.v_pending_requests IS 'All pending requests sorted oldest-first (for FIFO processing).';
COMMENT ON VIEW public.v_all_requests     IS 'All requests with submitter info joined — use for admin reports.';


-- ============================================================
-- SECTION 11: STUDENT RECORDS TABLE
-- Stores imported CSV data of graduates, undergraduates,
-- transferees, and dropouts for the Records section.
-- Run this in Supabase SQL Editor to add the student_records table.
-- ============================================================

CREATE TABLE IF NOT EXISTS public.student_records (
    id              UUID        PRIMARY KEY DEFAULT uuid_generate_v4(),
    student_name    TEXT        NOT NULL,
    student_id      TEXT,
    program         TEXT,
    status          TEXT        CHECK (status IN ('graduate', 'undergraduate', 'transferee', 'dropout')),
    year_level      TEXT,
    term            TEXT,
    school_year     TEXT,
    imported_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    imported_by     UUID        REFERENCES public.users(id) ON DELETE SET NULL
);

-- Indexes for common query patterns (search, filter by status, school year)
CREATE INDEX IF NOT EXISTS idx_sr_name        ON public.student_records(student_name);
CREATE INDEX IF NOT EXISTS idx_sr_status      ON public.student_records(status);
CREATE INDEX IF NOT EXISTS idx_sr_school_year ON public.student_records(school_year);
CREATE INDEX IF NOT EXISTS idx_sr_term        ON public.student_records(term);

COMMENT ON TABLE  public.student_records              IS 'Imported student database — graduates, undergraduates, transferees, dropouts.';
COMMENT ON COLUMN public.student_records.status       IS 'One of: graduate, undergraduate, transferee, dropout.';
COMMENT ON COLUMN public.student_records.imported_by  IS 'UUID of the admin who imported this record.';

-- Enable Row Level Security
ALTER TABLE public.student_records ENABLE ROW LEVEL SECURITY;

-- Admins can do everything on student_records
DROP POLICY IF EXISTS "records: admin all" ON public.student_records;
CREATE POLICY "records: admin all"
    ON public.student_records FOR ALL
    USING (
        EXISTS (
            SELECT 1 FROM public.users u
            WHERE u.id = auth.uid() AND u.role = 'admin'
        )
    );

-- Users (students) can read student_records for self-lookup purposes
DROP POLICY IF EXISTS "records: user read" ON public.student_records;
CREATE POLICY "records: user read"
    ON public.student_records FOR SELECT
    USING (auth.uid() IS NOT NULL);


