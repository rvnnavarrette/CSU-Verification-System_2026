-- Enable RLS
ALTER TABLE public.users                 ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.verification_requests ENABLE ROW LEVEL SECURITY;


-- ── users table policies ─────────────────────────────────────

DROP POLICY IF EXISTS "users: read own profile"  ON public.users;
DROP POLICY IF EXISTS "users: update own profile" ON public.users;
DROP POLICY IF EXISTS "users: admin read all"     ON public.users;
DROP POLICY IF EXISTS "users: insert own profile" ON public.users;

-- Users can read their own profile
CREATE POLICY "users: read own profile"
    ON public.users FOR SELECT
    USING (auth.uid() = id);

-- Users can update their own profile
CREATE POLICY "users: update own profile"
    ON public.users FOR UPDATE
    USING (auth.uid() = id);

-- Users can insert their own profile on signup
CREATE POLICY "users: insert own profile"
    ON public.users FOR INSERT
    WITH CHECK (auth.uid() = id);

-- Admins can read ALL user profiles
CREATE POLICY "users: admin read all"
    ON public.users FOR SELECT
    USING (
        EXISTS (
            SELECT 1 FROM public.users u
            WHERE u.id = auth.uid() AND u.role = 'admin'
        )
    );


-- ── verification_requests table policies ─────────────────────

DROP POLICY IF EXISTS "requests: user read own"   ON public.verification_requests;
DROP POLICY IF EXISTS "requests: user insert own" ON public.verification_requests;
DROP POLICY IF EXISTS "requests: admin read all"  ON public.verification_requests;
DROP POLICY IF EXISTS "requests: admin update"    ON public.verification_requests;
DROP POLICY IF EXISTS "requests: admin delete"    ON public.verification_requests;

-- Students can read ONLY their own requests
CREATE POLICY "requests: user read own"
    ON public.verification_requests FOR SELECT
    USING (auth.uid() = user_id);

-- Students can insert their own requests
CREATE POLICY "requests: user insert own"
    ON public.verification_requests FOR INSERT
    WITH CHECK (auth.uid() = user_id);

-- Students CANNOT update or delete (read-only after submission)
-- (No UPDATE/DELETE policies for users = denied by default)

-- Admins can read ALL requests
CREATE POLICY "requests: admin read all"
    ON public.verification_requests FOR SELECT
    USING (
        EXISTS (
            SELECT 1 FROM public.users u
            WHERE u.id = auth.uid() AND u.role = 'admin'
        )
    );

-- Admins can update any request (status, remarks, assessment)
CREATE POLICY "requests: admin update"
    ON public.verification_requests FOR UPDATE
    USING (
        EXISTS (
            SELECT 1 FROM public.users u
            WHERE u.id = auth.uid() AND u.role = 'admin'
        )
    );

-- Admins can delete requests (optional — remove if you want soft-delete only)
CREATE POLICY "requests: admin delete"
    ON public.verification_requests FOR DELETE
    USING (
        EXISTS (
            SELECT 1 FROM public.users u
            WHERE u.id = auth.uid() AND u.role = 'admin'
        )
    );


-- ============================================================
-- SECTION 7: AUTO-CREATE USER PROFILE ON SIGNUP
-- When a new user signs up via Supabase Auth, automatically
-- insert a row into public.users with role = 'user'.
-- ============================================================

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO public.users (id, email, display_name, role)
    VALUES (
        NEW.id,
        NEW.email,
        COALESCE(NEW.raw_user_meta_data->>'display_name', split_part(NEW.email, '@', 1)),
        'user'
    )
    ON CONFLICT (id) DO NOTHING;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trg_on_auth_user_created ON auth.users;
CREATE TRIGGER trg_on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();


-- ============================================================
-- SECTION 8: STORAGE BUCKET POLICIES
-- Run these AFTER creating the "verification-files" bucket
-- in Supabase → Storage → New Bucket (set to private).
-- ============================================================

-- Allow authenticated users to upload to their own folder
-- Path format: verificationFiles/{user_id}/{request_id}/{filename}
INSERT INTO storage.buckets (id, name, public)
VALUES ('verification-files', 'verification-files', false)
ON CONFLICT (id) DO NOTHING;

DROP POLICY IF EXISTS "storage: user upload own files"  ON storage.objects;
DROP POLICY IF EXISTS "storage: user read own files"   ON storage.objects;
DROP POLICY IF EXISTS "storage: admin read all files"  ON storage.objects;
DROP POLICY IF EXISTS "storage: admin delete files"    ON storage.objects;

-- Students can upload files under their own user_id folder
CREATE POLICY "storage: user upload own files"
    ON storage.objects FOR INSERT
    WITH CHECK (
        bucket_id = 'verification-files'
        AND auth.uid()::text = (string_to_array(name, '/'))[2]
    );

-- Students can read their own uploaded files
CREATE POLICY "storage: user read own files"
    ON storage.objects FOR SELECT
    USING (
        bucket_id = 'verification-files'
        AND auth.uid()::text = (string_to_array(name, '/'))[2]
    );

-- Admins can read ALL uploaded files
CREATE POLICY "storage: admin read all files"
    ON storage.objects FOR SELECT
    USING (
        bucket_id = 'verification-files'
        AND EXISTS (
            SELECT 1 FROM public.users u
            WHERE u.id = auth.uid() AND u.role = 'admin'
        )
    );

-- Admins can delete files (if needed)
CREATE POLICY "storage: admin delete files"
    ON storage.objects FOR DELETE
    USING (
        bucket_id = 'verification-files'
        AND EXISTS (
            SELECT 1 FROM public.users u
            WHERE u.id = auth.uid() AND u.role = 'admin'
        )
    );


