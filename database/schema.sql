-- Enable UUID generation (already enabled in Supabase by default)
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";


-- ============================================================
-- SECTION 2: USERS TABLE
-- Stores profile data for both students/clients and admin staff.
-- Linked 1:1 to Supabase Auth (auth.users).
-- ============================================================

CREATE TABLE IF NOT EXISTS public.users (
    id              UUID        PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email           TEXT        NOT NULL UNIQUE,
    display_name    TEXT        NOT NULL,
    role            TEXT        NOT NULL DEFAULT 'user' CHECK (role IN ('user', 'admin')),
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Index: fast lookup by email and role
CREATE INDEX IF NOT EXISTS idx_users_email ON public.users(email);
CREATE INDEX IF NOT EXISTS idx_users_role  ON public.users(role);

-- Comment
COMMENT ON TABLE  public.users              IS 'User profiles — students/clients and registrar staff.';
COMMENT ON COLUMN public.users.id           IS 'Matches auth.users.id (Supabase Auth UID).';
COMMENT ON COLUMN public.users.role         IS '"user" = student/client, "admin" = registrar staff.';


-- ============================================================
-- SECTION 3: VERIFICATION REQUESTS TABLE
-- One row per submitted verification request.
-- ============================================================

CREATE TABLE IF NOT EXISTS public.verification_requests (
    -- Primary key (generated by the app via crypto.randomUUID())
    id                  UUID        PRIMARY KEY DEFAULT uuid_generate_v4(),

    -- Who submitted it
    user_id             UUID        NOT NULL REFERENCES public.users(id) ON DELETE CASCADE,

    -- ── Personal / Academic Info ──────────────────────────
    student_name        TEXT        NOT NULL,           -- "LASTNAME, FIRSTNAME MIDDLENAME"
    degree_diploma      TEXT,                           -- e.g. "Bachelor of Science in IT"
    major_track         TEXT,                           -- e.g. "Web Development"
    student_status      TEXT        NOT NULL DEFAULT 'graduate'
                                    CHECK (student_status IN ('graduate', 'undergraduate')),
    date_of_graduation  TEXT,                           -- exact date OR "Approximate: YYYY"

    -- ── Enrollment Period ─────────────────────────────────
    term_started        TEXT,                           -- e.g. "1st Semester"
    school_year_started TEXT,                           -- e.g. "2019-2020"
    term_ended          TEXT,
    school_year_ended   TEXT,

    -- ── School Info ───────────────────────────────────────
    school_name         TEXT,
    school_address      TEXT,

    -- ── Uploaded Files (array of {name, url, type}) ───────
    uploaded_files      JSONB       NOT NULL DEFAULT '[]'::jsonb,

    -- ── Status & Review ───────────────────────────────────
    status              TEXT        NOT NULL DEFAULT 'pending'
                                    CHECK (status IN ('pending', 'verified', 'not_verified')),
    document_assessment TEXT,                           -- admin's assessment note
    admin_remarks       TEXT,                           -- admin's remarks/comments

    -- ── Timestamps ────────────────────────────────────────
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    reviewed_at         TIMESTAMPTZ                     -- set when admin changes status
);

-- Indexes: common query patterns in your JS
CREATE INDEX IF NOT EXISTS idx_vr_user_id    ON public.verification_requests(user_id);
CREATE INDEX IF NOT EXISTS idx_vr_status     ON public.verification_requests(status);
CREATE INDEX IF NOT EXISTS idx_vr_created_at ON public.verification_requests(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_vr_name       ON public.verification_requests(student_name);

-- GIN index for fast JSONB file queries
CREATE INDEX IF NOT EXISTS idx_vr_files_gin  ON public.verification_requests USING GIN (uploaded_files);

-- Comments
COMMENT ON TABLE  public.verification_requests                   IS 'Student/client verification requests submitted through the portal.';
COMMENT ON COLUMN public.verification_requests.uploaded_files    IS 'Array of {name, url, type} objects stored in Supabase Storage.';
COMMENT ON COLUMN public.verification_requests.status            IS '"pending" = awaiting review, "verified" = approved, "not_verified" = rejected.';
COMMENT ON COLUMN public.verification_requests.document_assessment IS 'Admin assessment of submitted documents.';


-- ============================================================
-- SECTION 4: AUTO-UPDATE updated_at TRIGGER
-- Automatically sets updated_at = NOW() on any row update.
-- ============================================================

-- Reusable trigger function
CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Attach to users
DROP TRIGGER IF EXISTS trg_users_updated_at ON public.users;
CREATE TRIGGER trg_users_updated_at
    BEFORE UPDATE ON public.users
    FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

-- Attach to verification_requests
DROP TRIGGER IF EXISTS trg_vr_updated_at ON public.verification_requests;
CREATE TRIGGER trg_vr_updated_at
    BEFORE UPDATE ON public.verification_requests
    FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();


-- ============================================================
-- SECTION 5: AUTO-SET reviewed_at TRIGGER
-- Sets reviewed_at when status changes from "pending".
-- ============================================================

CREATE OR REPLACE FUNCTION public.set_reviewed_at()
RETURNS TRIGGER AS $$
BEGIN
    IF OLD.status = 'pending' AND NEW.status <> 'pending' THEN
        NEW.reviewed_at = NOW();
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_vr_reviewed_at ON public.verification_requests;
CREATE TRIGGER trg_vr_reviewed_at
    BEFORE UPDATE ON public.verification_requests
    FOR EACH ROW EXECUTE FUNCTION public.set_reviewed_at();


-- ============================================================
-- SECTION 6: ROW LEVEL SECURITY (RLS)
-- Controls who can read/write which rows.
